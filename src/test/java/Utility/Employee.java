package Utility;

public class Employee {

    private String firstName;
    private String job;
    private int id;
    private String createdAt;
    private String updatedAt;

    public String getFirstName() {
        return firstName;
    }

    public String getJob() {
        return job;
    }

    public int getId() {
        return id;
    }

    public String getCreatedAt() {
        return createdAt;
    }

    public String getUpdatedAt() {
        return updatedAt;
    }
}
